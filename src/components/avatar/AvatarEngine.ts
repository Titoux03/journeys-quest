/**
 * AvatarEngine - Pure pixel art sprite data and color system
 * All sprites are 12x17 on a shared palette system
 * No emojis anywhere - 100% pixel art
 */

export type AvatarGender = 'male' | 'female';

// Color indices in sprites:
// 0 = transparent
// 1 = skin
// 2 = skin shadow
// 3 = clothing primary
// 4 = clothing shadow / shoes
// 5 = eye color
// 6 = hair color
// 7 = hair shadow
// 8 = accent/detail
// 9 = highlight

export interface AvatarColors {
  skin: string;
  skinShadow: string;
  eyes: string;
  clothing: string;
  clothingShadow: string;
  shoes: string;
  hair: string;
  hairShadow: string;
}

export interface AvatarConfig {
  gender: AvatarGender;
  skinIndex: number;
  eyeIndex: number;
  clothingIndex: number;
  shoesIndex: number;
  hairIndex: number;
}

